	sendMessage = async (data: DtoMessage): Promise<void> => {
		const fireStore = getFirestore();
		const messagesRef = collection(
			fireStore,
			FireStoreCollection.MESSAGES
		);

		try {
			return await setDoc(
				doc(messagesRef),
				JSON.parse(JSON.stringify(data))
			);
		} catch (e: any) {
			throw Error(`Something went wrong when sending message. ${e}`);
		}
	};

	getMessages = (
		lastSeen: string | null,
		batchSize: number
	): Observable<Message[]> => {
		return this.afs
			.collection<DtoMessage>(FireStoreCollection.MESSAGES, (ref) =>
				ref.orderBy('sendAt').startAfter(lastSeen).limit(batchSize)
			)
			.snapshotChanges()
			.pipe(
				distinctUntilChanged((prev, curr) => isEqual(prev, curr)),
				map<any, (DtoMessage & { id: string })[]>((result) => {
					return result.reduce((acc: any, cur: any) => {
						const id = cur.payload.doc.id;
						const data = cur.payload.doc.data();
						return { ...acc, [id]: data };
					}, {}) as (DtoMessage & { id: string })[];
				}),
				map<(DtoMessage & { id: string })[], Message[]>(
					(result) => Message.fromDtoCollection(result).data
				),
				catchError((error: any) => {
					console.error(error);
					return throwError(
						() => new Error('Error occurred while fetching messages.')
					).pipe(startWith([] as Message[]));
				})
			);
	};

